    private void salvarCadastroDeProdutoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCadastroDeProdutoButtonActionPerformed
        
       if(validaCamposAoCadastrarProduto()){
       UnidadeProduto unidadeProduto = (UnidadeProduto)listaDeUnidadesPesquisadas.getElementAt(JListaDeUnidades.getSelectedIndex());
        DepartamentoProduto dp = (DepartamentoProduto)listaDeDepartamentosPesquisados.getElementAt(JListaDeDepartamentos.getSelectedIndex());
      String produtoCadastrado = produtoDao.cadastrarProduto(
              descriçãoProdutoTextField.getText(), 
              unidadeProduto.getId(), 
              obsProdutoTextField.getText(), 
              dp.getId(), 
              usuarioLogado.getId(),
              cfopCadastroProdutoTextField.getText(),
              gtinProdutoCadastroTextField.getText());
      JOptionPane.showMessageDialog(rootPane, "Produto cadastrado\n Código:" + produtoCadastrado);
       }
    }//GEN-LAST:event_salvarCadastroDeProdutoButtonActionPerformed

