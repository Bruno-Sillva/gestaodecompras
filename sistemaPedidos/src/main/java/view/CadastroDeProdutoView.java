    private void buscarUnidadesProduto(String unidadePesquisada){
        DefaultListModel unidadesPesquisadas = new DefaultListModel();
        List<UnidadeProduto> unidadesDeProduto = produtoDao.buscarUnidadesProdutos();
        unidadesDeProduto.stream().forEach((unidadeProduto)->{
        if(unidadeProduto.toString().contains(unidadePesquisada.toUpperCase())){
            unidadesPesquisadas.addElement(unidadeProduto);
        }
        else if(unidadePesquisada.substring(0,1).contains(".")){
            unidadesPesquisadas.addElement(unidadeProduto);
        }
        });
        listaDeUnidadesPesquisadas = unidadesPesquisadas;
        JListaDeUnidades.setModel(unidadesPesquisadas);
    }
    
