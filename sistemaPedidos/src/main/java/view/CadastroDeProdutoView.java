    private void validarCamposAoEditarProduto(){
        StringBuilder erros = new StringBuilder();
        int qtErros = 0;
        if(produtoAEdtiar != null){
        if(descricaoProdutoEditarTextFiedl.getText().isEmpty()){
         qtErros++;
         erros.append("Descrição do produto não pode estar vazia \n");
        }
       
       if(unidadeProdutoEditarTextField.getText().isEmpty() || JListaDeUnidades.getSelectedIndex()<0){
           qtErros++;
         erros.append("Unidade do produto não foi selecionado da lista\n"); 
        }
        if(departamentoProdutoEditarTextField.getText().isEmpty() || JListaDeDepartamentos.getSelectedIndex()<0){
            qtErros++;
            erros.append("Departamento do produto não foi selecionado da lista\n");
        }
        if(qtErros>0){
            JOptionPane.showMessageDialog(rootPane, erros);
        }else{
        DepartamentoProduto dp = (DepartamentoProduto)listaDeDepartamentosPesquisados.getElementAt(JListaDeDepartamentos.getSelectedIndex());
        UnidadeProduto un = (UnidadeProduto)listaDeUnidadesPesquisadas.getElementAt(JListaDeUnidades.getSelectedIndex());
         Produto p = new Produto();
         p.setAtivo(desativarProdutoButton.isSelected());
         p.setId(produtoAEdtiar.getId());
         p.setName(descricaoProdutoEditarTextFiedl.getText());
         p.setCfop(cfopEditarProdutoTextField.getText());
         p.setDepartamentoId(dp.getId());
         p.setUnidadeId(un.getId());
         p.setGtin(gtinProdutoEditarTextField.getText());
         p.setObs(obsProdutoEditarTextField.getText());
         if(produtoDao.atualizarProduto(p, usuarioLogado)){
         JOptionPane.showMessageDialog(rootPane, "Produto atualizado");
         limparTelaEditarProduto();
         }
        }
        }
    }
    
