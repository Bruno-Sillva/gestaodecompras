    private void salvarCadastroFornecedorlButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCadastroFornecedorlButtonActionPerformed

        
        if(validaCamposCadastroFornecedor()){
            Fornecedor fornec = new Fornecedor();
            fornec.setNome(nomeFornecedorCadastrarTextField.getText());
            fornec.setFantasia(fantasiaFornecedorCadastrarTextField.getText());
            fornec.setBairro(bairroCadastarFornecedorTextField.getText());
            fornec.setMunicipio(municipioFornecedorFilialTextField.getText());
            fornec.setCep(cepCadastarFornecedorTextField.getText());
            fornec.setCnpj(cnpjFornecedorCadastrarTextField.getText());
            fornec.setNumero(numeroCadastarFornecedorTextField.getText());
            fornec.setInscricaoEstadual(iEFornecedorCadastrarTextField.getText());
            fornec.setEmail(emailCadastarFornecedorTextField.getText());
            fornec.setFone(foneCadastarFornecedorTextField.getText());
            fornec.setLogradouro(logradouroCadastarFornecedorTextField.getText());
            fornec.setUf(ufCadastarFornecedorTextField.getText());
          //  fornec.setTipoPessoa(TiposDePessoaEnum.valor(listaTiposDePessoa.getElementAt(jListaTiposDePessoa.getSelectedIndex()).toString()));
            fornec.setTipoPessoa(TiposDePessoaEnum.valor(tipoPessoaCadastroComboBox.getSelectedItem().toString()));
            FornecedorDao dao = new FornecedorDao();
            String fornecedorCadastrado = dao.cadastrarFornecedor(fornec,usuarioLogado);
            if(!fornecedorCadastrado.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Fornecedor " + fornecedorCadastrado + " - " + fornec.getNome() + " cadastrado.");
                limpaTela(cadastroFornecedor);
            }
        }
    }//GEN-LAST:event_salvarCadastroFornecedorlButtonActionPerformed

