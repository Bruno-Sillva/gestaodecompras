    private boolean validaCamposCadastroFornecedor(){
       StringBuilder erros = new StringBuilder();
       int erro = 0;
        if(!nomeFornecedorCadastrarTextField.getText().isEmpty() &&
           !fantasiaFornecedorCadastrarTextField.getText().isEmpty() &&
            !cnpjFornecedorCadastrarTextField.getText().isEmpty() &&
            !logradouroCadastarFornecedorTextField.getText().isEmpty() &&
             !cepCadastarFornecedorTextField.getText().isEmpty() &&
             !municipioFornecedorFilialTextField.getText().isEmpty() &&
             !ufCadastarFornecedorTextField.getText().isEmpty()){
         if(ufCadastarFornecedorTextField.getText().length() != 2){
             erro++;
             erros.append("UF deve ter dois caracteres \n");
         }   
         if(!cnpjFornecedorCadastrarTextField.getText().isEmpty()
             ){  
          try {
             Long cnpj = Long.valueOf(cnpjFornecedorCadastrarTextField.getText());
             } catch (Exception e) {
            erro++;
           erros.append("CPF/CNPJ com caracteres inválidos \n");
             }
         }
         if(tipoPessoaCadastroComboBox.getSelectedIndex()<0){
          erro++;
           erros.append("Tipo de pessoa não selecionado \n");   
         }
        if(!iEFornecedorCadastrarTextField.getText().isEmpty()){
            try {
                Long inscricaoEstadual =  Long.valueOf(iEFornecedorCadastrarTextField.getText());
            } catch (Exception e) {
             erro++;
           erros.append("Inscrição com caracteres inválidos \n");
            }
        }
        if(erro==0){
            return true;
        }else{
            JOptionPane.showMessageDialog(rootPane, erros);
            return false;
         
        }
        }else{  
         JOptionPane.showMessageDialog(rootPane, "Campos obrigatórios não preenchidos");
        return false;
        }
       
    }
    
    private void buscaFornecedoresTabela(){
        tabelaDeFornecedores.setRowCount(0);
        List<Fornecedor> fornecedores = fornecDao.fornecedores();
        fornecedores.stream().forEach((fornec)->{
         tabelaDeFornecedores.addRow(new Object[]{fornec.getId(), fornec.getNome(), fornec.getFantasia(), fornec.getUf(), fornec.getCnpj(), fornec.getTipoPessoa()});
        });
    }
    
    private void buscaFornecedorParaEdicao(String fornecedor){
       DefaultListModel listaFornec = new DefaultListModel();
        List<Fornecedor> fornecedores = fornecDao.fornecedores();
        fornecedores.stream().forEach((fornec)->{
        if(fornec.toString().contains(fornecedor.toUpperCase())){
         listaFornec.addElement(fornec);
        }
        });
        listaDeFornecedores = listaFornec;
       jListaDeFornecedores.setModel(listaFornec);
    }
    
    private void limpaTela(JPanel panel){
         Component[] componentes = panel.getComponents();
         for(Component c: componentes){
             if(c instanceof JTextField){
                 ((JTextField) c).setText("");
             }
             if(c instanceof JComboBox){
                 ((JComboBox) c).setSelectedIndex(-1);
             }
             fornecedorAEditar = null;
         }
    }
    
    
    
    
