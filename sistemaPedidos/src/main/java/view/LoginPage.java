    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
    if(arquivoUtils.checaSeOArquivoExiste()){
        Properties props = arquivoUtils.lerArquivoDeConfiguracao();
     if(DbUtils.testeDeConexao(props)){
       String usuario = loginTextField.getText();
       String senha = senhaTextField.getText();
        DbUtils dbUtils = new DbUtils(props);
       if(dbUtils.logar(usuario, senha) != null){
        //String[] args = null;
        Usuario usuarioLogado = dbUtils.logar(usuario, senha);
        HomePageView.main(usuarioLogado);
         this.dispose();
    }else{
        JOptionPane.showMessageDialog(rootPane, "Usuário ou senha inválidos");
    }
     }   
    }
   

    
    }//GEN-LAST:event_loginButtonActionPerformed

