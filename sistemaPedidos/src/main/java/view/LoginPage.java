    private void salvarConfigBancoDeDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarConfigBancoDeDadosActionPerformed
     
     if(validaCamposTesteBancoDeDados()){
        try {//jdbc:mariadb://{host}[:{port}]/[{database}]
        if(arquivoUtils.salvarArquivoDeConfiguracao(propriedadesBancoDeDados())){
            JOptionPane.showMessageDialog(null, "Configurações salvas");
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }  
     }
       
    }//GEN-LAST:event_salvarConfigBancoDeDadosActionPerformed

