    private void preencheCamposConfiguracaoBancoDeDados(){
        if(arquivoUtils.checaSeOArquivoExiste()){
          Properties props = arquivoUtils.lerArquivoDeConfiguracao();
         urlBancoDeDadosTextField.setText(props.getProperty(ConfiguracaoBancoDeDadosEnum.JDBC_URL.getValor())); 
         usuarioBancoDeDadosTextField.setText(props.getProperty(ConfiguracaoBancoDeDadosEnum.JDBC_USERNAME.getValor()));
         senhaBancoDeDadosTextField.setText(props.getProperty(ConfiguracaoBancoDeDadosEnum.JDBC_PASSWORD.getValor()));
         driverBancoDeDadosTextField.setText(props.getProperty(ConfiguracaoBancoDeDadosEnum.getJDBC_DRIVER().getValor()));
        }
    }
    
    private void testarConfiguracaoBancoDeDados(){
       if(arquivoUtils.checaSeOArquivoExiste()){
         Properties props = arquivoUtils.lerArquivoDeConfiguracao();
         if(DbUtils.testeDeConexao(props)){
         JOptionPane.showMessageDialog(null, "Conexão bem sucedida");
         }else{
              JOptionPane.showMessageDialog(null, "Falha ao conectar ao banco de dados");
         }
       } 
       else{
          if(validaCamposTesteBancoDeDados()){
          Properties props = propriedadesBancoDeDados();
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_DRIVER.getValor(), driverBancoDeDadosTextField.getText());
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_USERNAME.getValor(),usuarioBancoDeDadosTextField.getText());
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_PASSWORD.getValor(), senhaBancoDeDadosTextField.getText());
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_URL.getValor(), urlBancoDeDadosTextField.getText());
          if(DbUtils.testeDeConexao(props)){
            JOptionPane.showMessageDialog(null, "Conexão bem sucedida");   
          }else{
               JOptionPane.showMessageDialog(null, "Falha ao conectar ao banco de dados");
          }
          
          } 
       }
    }
    
    
    private boolean validaCamposTesteBancoDeDados(){
        return  !urlBancoDeDadosTextField.getText().isEmpty() &&
                !usuarioBancoDeDadosTextField.getText().isEmpty() &&
                !senhaBancoDeDadosTextField.getText().isEmpty() &&
                !driverBancoDeDadosTextField.getText().isEmpty();
    }
    
    
    private Properties propriedadesBancoDeDados(){
        Properties props = new Properties();
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_DRIVER.getValor(), driverBancoDeDadosTextField.getText());
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_USERNAME.getValor(),usuarioBancoDeDadosTextField.getText());
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_PASSWORD.getValor(), senhaBancoDeDadosTextField.getText());
          props.setProperty(ConfiguracaoBancoDeDadosEnum.JDBC_URL.getValor(), urlBancoDeDadosTextField.getText());
          return props;
    }
    
